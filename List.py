# Mutable = Can change


grocery = ["Apple","Mango","Orange"]
print(grocery)
print(grocery[2])

num = [2,11,9,8,7]
print(num[0:])
num.append(10)
num.insert(3,60)
print(num)

num.remove(8)
print(num)

num.pop()
print(num)

print(min(num))

print(max(num))

num.sort()
print(num)

num.reverse()
print(num)

