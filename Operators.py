
# Operators in Python

# Arithmetic Operators
print("Arithmetic Operators:")
print("5+6 is",5+6)
print("5-6 is ",5-6)
print("5*6 is",5*6)
print("5**6 is ",5**6)
print("5%3 is ",5%3)
print("5/6 is",5/6)
print("15//6 is",15//6)

# Assignment Operators
print("Assignment Operators:")
x=5
x+=7   # -= , /= , %= , *=
print(x)

# Comparison Operators

print("Comparison Operators:")
y=5
print(y!=5)   # == , > , < , >= , <=

# Logiclal Operators

print("Logical Operators:")
a = True
b = False
print(a and b)    # or

# Identity Operators

print("Identity Operators:")
a = True
b = False

print(a is not b)

# Membership Operators

print("Membership Operators:")
list = [3,3,2,10,20]

print(30 in list)

# Bitwise Operators
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11

print("Bitwise Operators:")
print(0 & 1)
print(0 | 3)